# A list of basic shopping items
shopping_list = ["milk", "eggs", "bread", "butter"]

# create a more poetic shopping list
poetic_shopping_list = ["milk of the morning", "eggs of the dawn", "bread of the earth", "butter of the sun"]

# print the first item in the shopping list
print(poetic_shopping_list[0])

# print the last item in the shopping list
print(poetic_shopping_list[-1])

# add a new item to the shopping list
poetic_shopping_list.append("honey of the bees") 

# print the updated shopping list
print(poetic_shopping_list)     

# remove an item from the shopping list
poetic_shopping_list.remove("bread of the earth")   

# print the updated shopping list
print(poetic_shopping_list)