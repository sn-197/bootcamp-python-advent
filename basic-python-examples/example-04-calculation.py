# This example demonstrates basic arithmetic operations in Python.

a = 15
b = 4

# Addition
sum_result = a + b

# Subtraction
difference_result = a - b

# Multiplication
product_result = a * b

# Division
quotient_result = a / b

print("Addition:", sum_result)
print("Subtraction:", difference_result)
print("Multiplication:", product_result)
print("Division:", quotient_result)